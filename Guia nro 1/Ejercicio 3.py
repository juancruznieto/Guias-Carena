#Desarrollar un algoritmo para calcular la superficie de un cuadrado (base por altura):
print('calculo de la superficie de un cuadrado')

#Mostrar el mensaje de ingreso y guardar en base.
base = int(input('ingrese un valor para base: '))

#Mostrar mensaje de ingreso y guardar en altura.
altura = int(input('ingrese un valor para altura: '))

#Calcular base * altura y guardar en superficie.
superficie = base * altura

#Mostrar mensaje y valor de superficie
print('valor de la superficie', superficie)
