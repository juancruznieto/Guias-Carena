#Desarrollar un algoritmo para calcular el promedio de 4 n√∫meros ingresados por teclado:

#Mostrar mensaje de ingreso y guardar en uno.
uno = int(input('ingrese un valor para uno: '))
#Mostrar mensaje de ingreso y guardar en dos.
dos = int(input('ingrese un valor para dos: '))
#Mostrar mensaje de ingreso y guardar en tres.
tres = int(input('ingrese un valor para tres: '))
#Mostrar mensaje de ingreso y guardar en cuatro.
cuatro = int(input('ingrese un valor para cuatro: '))
#Sumar, dividir por cuatro y guardar en una variable resultado.
promedio = (uno + dos + tres + cuatro) /4
#Mostrar mensaje de resultado y el valor de resultado.
print('el promedio de los valores ingresado es: ', promedio)